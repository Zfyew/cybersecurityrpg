// game state
let player = null;
let currentLevel = null;
let selectedLog = null;
let aiMode = false;
let apiKey = null;

// static hints for base game
const BASE_HINTS = {
    1: "Look for passwords that are short, use common words, or are just numbers. Length and complexity matter.",
    2: "Think about whether this service should ever be exposed. Some protocols are outdated and insecure by design.",
    3: "Look for patterns that don't match normal user behaviour — unusual IPs, impossible login timing, or bulk data access."
}

function animateIntro() {
    const lines = document.querySelectorAll('.type-line');
    lines.forEach((line, i) => {
        line.style.animationDelay = `${i * 0.4}s`;
    });
}

animateIntro();

async function startGame() {
    const name = document.getElementById('player-name').value.trim();
    if (!name) {
        document.getElementById('player-name').focus();
        return;
    }

    const key = document.getElementById('api-key').value.trim();
    if (key) {
        apiKey = key;
        aiMode = true;
    }

    const res = await fetch('/api/start', {
        method: 'POST',
        headers: { 'Content-Type': 'application/json' },
        body: JSON.stringify({ name, ai_mode: aiMode })
    });

    const data = await res.json();
    player = data.player;

    document.getElementById('intro-screen').classList.remove('active');
    document.getElementById('game-screen').classList.add('active');
    updateHUD();
}

document.getElementById('player-name').addEventListener('keydown', e => {
    if (e.key === 'Enter') startGame();
});

function updateHUD() {
    if (!player) return;
    document.getElementById('hud-name').textContent = player.name;
    document.getElementById('hud-health').textContent = player.health;
    document.getElementById('hud-score').textContent = player.score;
    document.getElementById('hud-tools').textContent = player.inventory.length ? player.inventory.join(', ') : 'None';

    // show AI mode badge in HUD if active
    const nameEl = document.getElementById('hud-name');
    if (aiMode && !nameEl.querySelector('.ai-mode-badge')) {
        const badge = document.createElement('span');
        badge.className = 'ai-mode-badge';
        badge.textContent = 'AI MODE';
        nameEl.appendChild(badge);
    }

    const bar = document.getElementById('health-bar');
    bar.style.width = player.health + '%';
    bar.style.background = player.health > 60 ? '#00ff41' : player.health > 30 ? '#ffbd2e' : '#ff4444';

    const completed = player.completed || [];

    for (let i = 1; i <= 3; i++) {
        const card = document.getElementById(`level-${i}-card`);
        const status = document.getElementById(`level-${i}-status`);

        if (completed.includes(i)) {
            card.classList.remove('locked');
            card.style.opacity = '0.4';
            card.style.cursor = 'not-allowed';
            status.textContent = 'COMPLETE';
            card.onclick = null;
        } else if (i <= player.level) {
            card.classList.remove('locked');
            card.style.opacity = '1';
            card.style.cursor = 'pointer';
            status.textContent = 'AVAILABLE';
            card.onclick = () => loadLevel(i);
        } else {
            card.classList.add('locked');
            card.style.opacity = '1';
            status.textContent = 'LOCKED';
            card.onclick = null;
        }
    }

    if (player.health <= 0) gameOver(false);
}

function showLevelSelect() {
    document.getElementById('level-panel').classList.remove('active');
    document.getElementById('level-select').classList.add('active');
    document.getElementById('feedback').classList.add('hidden');
}

async function loadLevel(num) {
    currentLevel = num;
    selectedLog = null;

    const res = await fetch(`/api/level/${num}`);
    if (!res.ok) return;
    const data = await res.json();

    document.getElementById('level-select').classList.remove('active');
    document.getElementById('level-panel').classList.add('active');
    document.getElementById('level-title').textContent = `// ${data.title.toUpperCase()}`;
    document.getElementById('level-desc').textContent = data.description;
    document.getElementById('feedback').classList.add('hidden');
    document.getElementById('feedback').className = 'feedback hidden';

    const content = document.getElementById('level-content');
    content.innerHTML = '';

    // show mode indicator
    const modeEl = document.createElement('p');
    modeEl.className = `mode-indicator ${aiMode ? 'ai' : ''}`;
    modeEl.textContent = aiMode ? '// AI MODE — scenarios generated by Claude' : '// STANDARD MODE';
    content.appendChild(modeEl);

    // hint button — uses Claude if in AI mode, static hint if not
    const hintRow = document.createElement('div');
    hintRow.style.marginBottom = '20px';
    const hintBtn = document.createElement('button');
    hintBtn.className = 'hint-btn';
    hintBtn.textContent = aiMode ? '⚡ ASK CLAUDE FOR A HINT' : '? GET HINT';
    hintBtn.onclick = () => showHint(num, data);
    hintRow.appendChild(hintBtn);

    const hintBox = document.createElement('div');
    hintBox.className = 'hint-box';
    hintBox.id = 'hint-box';
    hintRow.appendChild(hintBox);
    content.appendChild(hintRow);

    if (data.type === 'password') renderPasswordLevel(data, content);
    else if (data.type === 'port') renderPortLevel(data, content);
    else if (data.type === 'logs') renderLogLevel(data, content);
}

async function showHint(levelNum, levelData) {
    const hintBox = document.getElementById('hint-box');
    hintBox.classList.add('visible');
    hintBox.textContent = aiMode ? 'Asking Claude...' : BASE_HINTS[levelNum];

    if (!aiMode) return;

    // build a prompt based on what's currently on screen
    let context = '';
    if (levelData.type === 'password') {
        const passwords = levelData.passwords.map(p => p.password).join(', ');
        context = `The player is looking at these passwords: ${passwords}. Give a short hint about what makes a password weak without revealing the answer.`;
    } else if (levelData.type === 'port') {
        context = `The player is deciding whether to block port ${levelData.port} running ${levelData.service}. Give a short hint about this service without revealing the answer.`;
    } else if (levelData.type === 'logs') {
        context = `The player is analysing these log entries: ${levelData.logs.join(' | ')}. Give a short hint about what to look for in suspicious logs without revealing which one is the attacker.`;
    }

    try {
        const res = await fetch('/api/ai_hint', {
            method: 'POST',
            headers: { 'Content-Type': 'application/json' },
            body: JSON.stringify({ context, api_key: apiKey })
        });
        const data = await res.json();
        hintBox.textContent = data.hint || 'Could not get hint. Check your API key.';
    } catch (e) {
        hintBox.textContent = 'Could not reach Claude. Check your API key.';
    }
}

function renderPasswordLevel(data, container) {
    const grid = document.createElement('div');
    grid.className = 'password-grid';

    data.passwords.forEach((p, i) => {
        const btn = document.createElement('button');
        btn.className = 'password-btn';
        btn.textContent = p.password;
        btn.onclick = () => submitAnswer(i);
        grid.appendChild(btn);
    });

    container.appendChild(grid);
}

function renderPortLevel(data, container) {
    const output = document.createElement('div');
    output.className = 'scan-output';

    const lines = [
        `> initiating port scan...`,
        `> target: 192.168.1.${Math.floor(Math.random() * 254) + 1}`,
        `> scanning common ports...`,
        `>`,
        `> PORT     SERVICE    STATE`,
        `> ──────────────────────────`,
        `> ${String(data.port).padEnd(9)}${data.service.padEnd(11)}OPEN`,
        `>`,
        `> scan complete.`
    ];

    lines.forEach((line, i) => {
        const p = document.createElement('p');
        p.className = 'scan-line';
        p.textContent = line;
        output.appendChild(p);
        setTimeout(() => p.classList.add('visible'), i * 200);
    });

    container.appendChild(output);

    const actions = document.createElement('div');
    actions.className = 'port-actions';

    const blockBtn = document.createElement('button');
    blockBtn.className = 'btn-block';
    blockBtn.textContent = 'BLOCK PORT';
    blockBtn.onclick = () => submitAnswer(true);

    const allowBtn = document.createElement('button');
    allowBtn.className = 'btn-allow';
    allowBtn.textContent = 'LEAVE OPEN';
    allowBtn.onclick = () => submitAnswer(false);

    actions.appendChild(blockBtn);
    actions.appendChild(allowBtn);
    container.appendChild(actions);
}

function renderLogLevel(data, container) {
    const intro = document.createElement('p');
    intro.style.cssText = 'color:#888; margin-bottom:16px; font-size:13px;';
    intro.textContent = 'Click the suspicious log entry then confirm your selection.';
    container.appendChild(intro);

    data.logs.forEach((log, i) => {
        const entry = document.createElement('div');
        entry.className = 'log-entry';
        entry.textContent = log;
        entry.onclick = () => {
            document.querySelectorAll('.log-entry').forEach(e => e.classList.remove('selected'));
            entry.classList.add('selected');
            selectedLog = i;
        };
        container.appendChild(entry);
    });

    const confirmBtn = document.createElement('button');
    confirmBtn.style.marginTop = '20px';
    confirmBtn.textContent = 'CONFIRM SELECTION';
    confirmBtn.onclick = () => {
        if (selectedLog === null) return;
        submitAnswer(selectedLog);
    };
    container.appendChild(confirmBtn);
}

async function submitAnswer(answer) {
    const res = await fetch('/api/answer', {
        method: 'POST',
        headers: { 'Content-Type': 'application/json' },
        body: JSON.stringify({ level: currentLevel, answer, ai_mode: aiMode, api_key: apiKey })
    });

    const data = await res.json();
    player = data.player;
    updateHUD();

    const feedback = document.getElementById('feedback');
    feedback.classList.remove('hidden', 'wrong');

    if (data.correct) {
        feedback.textContent = `✓ Correct. ${data.reason} +${data.points} points.`;

        // if AI mode, get personalised feedback from Claude
        if (aiMode && data.ai_feedback) {
            const aiFeedback = document.createElement('p');
            aiFeedback.style.cssText = 'margin-top:10px; color:#888; font-size:12px;';
            aiFeedback.textContent = `Claude: ${data.ai_feedback}`;
            feedback.appendChild(aiFeedback);
        }

        if (player.level > currentLevel || currentLevel === 3) {
            const nextBtn = document.createElement('button');
            nextBtn.className = 'next-btn';
            nextBtn.textContent = currentLevel === 3 ? 'VIEW RESULTS' : 'NEXT MISSION →';
            nextBtn.onclick = currentLevel === 3 ? () => gameOver(true) : showLevelSelect;
            feedback.appendChild(document.createElement('br'));
            feedback.appendChild(nextBtn);
        }
    } else {
        feedback.classList.add('wrong');
        feedback.textContent = `✗ Wrong call. ${data.reason} -25 health.`;
        const retryBtn = document.createElement('button');
        retryBtn.className = 'next-btn';
        retryBtn.textContent = 'TRY AGAIN';
        retryBtn.onclick = () => loadLevel(currentLevel);
        feedback.appendChild(document.createElement('br'));
        feedback.appendChild(retryBtn);
    }
}

function gameOver(won) {
    document.getElementById('game-screen').classList.remove('active');
    document.getElementById('gameover-screen').classList.add('active');

    const msg = document.getElementById('gameover-msg');
    if (won) {
        msg.innerHTML = `
            <p style="color:#00ff41; font-size:18px; margin-bottom:16px; letter-spacing:4px;">// MISSION COMPLETE</p>
            <p style="margin-bottom:8px;">Agent: ${player.name}</p>
            <p style="margin-bottom:8px;">Final score: ${player.score}</p>
            <p style="margin-bottom:8px;">Health remaining: ${player.health}</p>
            <p style="margin-bottom:20px;">Tools acquired: ${player.inventory.join(', ') || 'None'}</p>
            <p style="color:#444; font-size:11px; letter-spacing:2px; margin-bottom:4px;">// PERFORMANCE RATING</p>
            <p style="color:#00ff41; font-size:22px; margin-bottom:20px;">${getRating(player.score, player.health)}</p>
            <p style="color:#888; font-size:12px;">All threats neutralised. Good work, ${player.name}.</p>
        `;
    } else {
        msg.innerHTML = `
            <p style="color:#ff4444; font-size:18px; margin-bottom:16px; letter-spacing:4px;">// AGENT DOWN</p>
            <p style="margin-bottom:8px;">Agent: ${player.name}</p>
            <p style="margin-bottom:8px;">Final score: ${player.score}</p>
            <p style="color:#888; margin-top:12px; font-size:12px;">You ran out of health. The breach was not contained.</p>
        `;
    }

    const restartBtn = document.createElement('button');
    restartBtn.style.marginTop = '24px';
    restartBtn.textContent = 'RESTART';
    restartBtn.onclick = () => location.reload();
    document.getElementById('gameover-msg').appendChild(restartBtn);
}

function getRating(score, health) {
    const total = score + health;
    if (total >= 500) return 'S — ELITE ANALYST';
    if (total >= 400) return 'A — SENIOR ANALYST';
    if (total >= 300) return 'B — ANALYST';
    return 'C — JUNIOR ANALYST';
}